def _draw_cover(pdf, idea: str, created_at: str) -> None:
    pdf.add_page()

    # Dark header band
    pdf.set_fill_color(*_DARK)
    pdf.rect(0, 0, pdf.w, 52, "F")

    # FoundryBuild wordmark in the band
    pdf.set_y(14)
    pdf.set_font("Helvetica", "B", 11)
    pdf.set_text_color(200, 205, 215)
    pdf.cell(0, 7, "FoundryBuild AI", align="C", new_x="LMARGIN", new_y="NEXT")
    pdf.set_font("Helvetica", "", 8)
    pdf.set_text_color(100, 110, 130)
    pdf.cell(0, 5, "foundrybuild.xyz", align="C")

    # Main title area (white background, subtle border)
    pdf.set_fill_color(255, 255, 255)
    pdf.set_draw_color(220, 222, 228)
    pdf.rect(_L_MARGIN, 65, pdf.w - _L_MARGIN - _R_MARGIN, 85, "FD")

    pdf.set_y(75)
    pdf.set_font("Helvetica", "B", 20)
    pdf.set_text_color(*_DARK)
    safe_idea = _latin1_safe(idea[:100])
    pdf.multi_cell(pdf.w - _L_MARGIN - _R_MARGIN - 10, 12, safe_idea, align="C")

    pdf.ln(6)
    pdf.set_font("Helvetica", "", 11)
    pdf.set_text_color(*_GRAY)
    pdf.cell(0, 7, "Engineering Blueprint", align="C", new_x="LMARGIN", new_y="NEXT")

    # Metadata row
    pdf.set_y(160)
    pdf.set_font("Helvetica", "", 9)
    pdf.set_text_color(*_GRAY)
    date_str = created_at[:10] if created_at else "—"
    pdf.cell(0, 6, f"Generated  {date_str}", align="C", new_x="LMARGIN", new_y="NEXT")

    # Bottom accent strip
    pdf.set_fill_color(*_ACCENT)
    pdf.rect(0, pdf.h - 8, pdf.w, 8, "F")

    pdf.set_text_color(0, 0, 0)
    pdf.set_draw_color(0, 0, 0)


