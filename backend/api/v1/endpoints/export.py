def _ensure_space(pdf, min_mm: float = 35.0) -> None:
    """Add a new page if less than min_mm remains before the bottom margin."""
    if pdf.get_y() > pdf.h - pdf.b_margin - min_mm:
        pdf.add_page()


def _render_table_rows(pdf, rows: list) -> None:
    if not rows:
        return
    num_cols = max(len(r) for r in rows)
    if not num_cols:
        return

    page_w = pdf.w - pdf.l_margin - pdf.r_margin

    # Proportional column widths based on max content length per column
    col_maxlen = [0] * num_cols
    for row in rows:
        for c in range(min(len(row), num_cols)):
            col_maxlen[c] = max(col_maxlen[c], len(row[c].strip()))
    total_len = sum(col_maxlen) or 1
    raw_widths = [(l / total_len) * page_w for l in col_maxlen]
    # Enforce minimum 15mm per column
    col_widths = [max(w, 15.0) for w in raw_widths]
    scale = page_w / sum(col_widths)
    col_widths = [w * scale for w in col_widths]

    cell_h = 6.5

    for idx, row in enumerate(rows):
        is_header = idx == 0

        # Check page break before each row
        if pdf.get_y() + cell_h > pdf.h - pdf.b_margin:
            pdf.add_page()
            # Re-render header row after page break
            if not is_header:
                _render_header_row(pdf, rows[0], col_widths, cell_h)

        if is_header:
            _render_header_row(pdf, row, col_widths, cell_h)
        else:
            pdf.set_font("Helvetica", "", 8.5)
            fill_color = (248, 249, 251) if idx % 2 == 0 else (255, 255, 255)
            pdf.set_fill_color(*fill_color)
            pdf.set_text_color(30, 30, 40)
            for c in range(num_cols):
                raw = row[c].strip() if c < len(row) else ""
                cell_text = _latin1_safe(_strip_inline(raw))
                max_chars = max(int(col_widths[c] / 1.65), 10)
                if len(cell_text) > max_chars:
                    cell_text = cell_text[:max_chars - 2] + ".."
                pdf.cell(col_widths[c], cell_h, cell_text, border=1, fill=True, new_x="RIGHT", new_y="TOP")
            pdf.ln(cell_h)

    pdf.set_font("Helvetica", size=11)
    pdf.set_fill_color(255, 255, 255)
    pdf.set_text_color(0, 0, 0)
    pdf.ln(3)


def _render_header_row(pdf, row: list, col_widths: list, cell_h: float) -> None:
    num_cols = len(col_widths)
    pdf.set_font("Helvetica", "B", 8.5)
    pdf.set_fill_color(*_DARK)
    pdf.set_text_color(220, 222, 230)
    for c in range(num_cols):
        raw = row[c].strip() if c < len(row) else ""
        cell_text = _latin1_safe(_strip_inline(raw))
        max_chars = max(int(col_widths[c] / 1.65), 10)
        if len(cell_text) > max_chars:
            cell_text = cell_text[:max_chars - 2] + ".."
        pdf.cell(col_widths[c], cell_h, cell_text, border=1, fill=True, new_x="RIGHT", new_y="TOP")
    pdf.ln(cell_h)


def _render_markdown_pdf(pdf, markdown_text: str) -> None:
    """Render markdown line-by-line to fpdf2, bypassing write_html()."""
    lines = markdown_text.splitlines()
    i = 0
    consecutive_blanks = 0

    while i < len(lines):
        raw = lines[i]
        stripped = raw.strip()

        # --- Blank line ---
        if not stripped:
            consecutive_blanks += 1
            if consecutive_blanks == 1:
                pdf.ln(2)
            i += 1
            continue
        consecutive_blanks = 0

        # --- Fenced code block ---
        if stripped.startswith("```"):
            i += 1
            code_lines = []
            while i < len(lines) and not lines[i].strip().startswith("```"):
                code_lines.append(lines[i])
                i += 1
            if code_lines:
                _ensure_space(pdf, 20)
                pdf.set_font("Courier", "", 8)
                pdf.set_fill_color(240, 241, 244)
                pdf.set_text_color(30, 30, 40)
                for cl in code_lines:
                    safe = _latin1_safe(cl) or " "
                    # Truncate very long lines
                    if len(safe) > 100:
                        safe = safe[:97] + "..."
                    pdf.multi_cell(0, 4.5, safe, fill=True)
                pdf.set_font("Helvetica", size=11)
                pdf.set_fill_color(255, 255, 255)
                pdf.set_text_color(0, 0, 0)
                pdf.ln(2)
            i += 1  # closing ```
            continue

        # --- Markdown table ---
        if stripped.startswith("|"):
            table_rows: list[list[str]] = []
            while i < len(lines) and lines[i].strip().startswith("|"):
                row = lines[i].strip()
                if not re.fullmatch(r"\|[-| :]+\|", row):
                    cells = [c.strip() for c in row.strip("|").split("|")]
                    table_rows.append(cells)
                i += 1
            if table_rows:
                _ensure_space(pdf, 25)
                pdf.ln(2)
                _render_table_rows(pdf, table_rows)
            continue

        # --- H1 ---
        if re.match(r"^# [^#]", stripped):
            _ensure_space(pdf, 50)
            heading = _latin1_safe(_strip_inline(stripped[2:]))
            # Decorative full-width bar
            pdf.set_fill_color(*_DARK)
            pdf.rect(pdf.l_margin - 2, pdf.get_y(), pdf.w - pdf.l_margin - pdf.r_margin + 4, 11, "F")
            pdf.set_font("Helvetica", "B", 13)
            pdf.set_text_color(230, 232, 238)
            pdf.cell(0, 11, f"  {heading}", new_x="LMARGIN", new_y="NEXT")
            pdf.set_fill_color(255, 255, 255)
            pdf.set_text_color(0, 0, 0)
            pdf.set_font("Helvetica", size=11)
            pdf.ln(3)

        # --- H2 ---
        elif re.match(r"^## [^#]", stripped):
            _ensure_space(pdf, 38)
            heading = _latin1_safe(_strip_inline(stripped[3:]))
            pdf.ln(1)
            pdf.set_font("Helvetica", "B", 12)
            pdf.set_text_color(*_ACCENT)
            pdf.multi_cell(0, 7, heading)
            # Thin underline
            y = pdf.get_y()
            pdf.set_draw_color(*_ACCENT)
            pdf.set_line_width(0.4)
            pdf.line(pdf.l_margin, y, pdf.l_margin + 50, y)
            pdf.set_line_width(0.2)
            pdf.set_draw_color(0, 0, 0)
            pdf.set_text_color(0, 0, 0)
            pdf.set_font("Helvetica", size=11)
            pdf.ln(2)

        # --- H3 ---
        elif re.match(r"^### [^#]", stripped):
            _ensure_space(pdf, 28)
            heading = _latin1_safe(_strip_inline(stripped[4:]))
            pdf.ln(1)
            pdf.set_font("Helvetica", "B", 11)
            pdf.set_text_color(40, 50, 70)
            pdf.multi_cell(0, 6.5, heading)
            pdf.set_text_color(0, 0, 0)
            pdf.set_font("Helvetica", size=11)
            pdf.ln(1)

        # --- H4+ ---
        elif re.match(r"^#{4,} ", stripped):
            _ensure_space(pdf, 22)
            m = re.match(r"^(#{4,}) ", stripped)
            rest = _latin1_safe(_strip_inline(stripped[len(m.group(1)) + 1:]))
            pdf.set_font("Helvetica", "BI", 10)
            pdf.set_text_color(60, 70, 90)
            pdf.multi_cell(0, 6, rest)
            pdf.set_text_color(0, 0, 0)
            pdf.set_font("Helvetica", size=11)

        # --- Bullet list item ---
        elif re.match(r"^[-*•●] ", stripped):
            text = _latin1_safe(_strip_inline(stripped[2:]))
            pdf.set_font("Helvetica", "", 10.5)
            pdf.set_text_color(30, 30, 40)
            # Indent and bullet
            x_before = pdf.get_x()
            pdf.set_x(pdf.l_margin + 4)
            pdf.multi_cell(pdf.w - pdf.l_margin - pdf.r_margin - 4, 5.5, f"•  {text}")
            pdf.set_text_color(0, 0, 0)

        # --- Numbered list item ---
        elif re.match(r"^\d+\. ", stripped):
            m = re.match(r"^(\d+)\. ", stripped)
            text = _latin1_safe(_strip_inline(stripped[len(m.group(0)):]))
            pdf.set_font("Helvetica", "", 10.5)
            pdf.set_text_color(30, 30, 40)
            pdf.set_x(pdf.l_margin + 4)
            pdf.multi_cell(pdf.w - pdf.l_margin - pdf.r_margin - 4, 5.5, f"{m.group(1)}.  {text}")
            pdf.set_text_color(0, 0, 0)

        # --- Horizontal rule ---
        elif re.match(r"^[-_*]{3,}$", stripped):
            pdf.ln(2)
            y = pdf.get_y()
            pdf.set_draw_color(200, 202, 208)
            pdf.line(pdf.l_margin, y, pdf.w - pdf.r_margin, y)
            pdf.set_draw_color(0, 0, 0)
            pdf.ln(3)

        # --- Regular paragraph ---
        else:
            text = _latin1_safe(_strip_inline(stripped))
            pdf.set_font("Helvetica", "", 10.5)
            pdf.set_text_color(30, 30, 40)
            pdf.multi_cell(0, 5.5, text)
            pdf.set_text_color(0, 0, 0)
            pdf.ln(1)

        i += 1


